package constante;

public class Constante {
    public static void main(String[] args) {
        // Une Constante est un space resevée en mémoire qui ne chenge pas tout au long du programme
        final int numero =15;  // la déclartion se fait avec le mot final
        final char mot = '2';

        System.out.println(numero);
        System.out.println(mot);
    }

}
